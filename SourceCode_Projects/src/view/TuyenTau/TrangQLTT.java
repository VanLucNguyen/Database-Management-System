    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tableTT.getSelectedRow();
        if (selectedIndex == -1){
            JOptionPane.showMessageDialog(TrangQLTT.this, "Vui lòng chọn tuyến tàu cần sửa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            String maTT = String.valueOf(tableTT.getValueAt(selectedIndex, 0));
            new SuaTT(maTT).setVisible(true);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập tên tuyến tàu cần tra cứu:");
        if(input != null && input.length() > 0){
            tuyentaus = ttService.findTTByTenTT(macty, input);
            tblModel.setRowCount(0);
            setTableData(tuyentaus);
        } else{
            tblModel.setRowCount(0);
            setTableData(ttService.getAllTTs(macty));
        }
    }//GEN-LAST:event_btnTraActionPerformed

