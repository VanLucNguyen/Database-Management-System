    private void setTableData(List<TuyenTau> tuyentaus){
        for (TuyenTau tuyentau: tuyentaus){
            tblModel.addRow(new Object[]{tuyentau.getMaTT(), tuyentau.getTenTT(), tuyentau.getMaCty(), tuyentau.getGaBD(), tuyentau.getGaCuoi(), tuyentau.getLoaiTuyen(), tuyentau.getGiaTuyen(), tuyentau.getTinhTrang(), tuyentau.getTGbatdau(), tuyentau.getTGkethuc(), tuyentau.getTGcho()});
        }
    }

