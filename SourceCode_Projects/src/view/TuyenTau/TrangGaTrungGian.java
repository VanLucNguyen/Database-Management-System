    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tblGTG.getSelectedRow();
        if (selectedIndex == -1){
            JOptionPane.showMessageDialog(TrangGaTrungGian.this, "Vui lòng chọn ga cần sửa!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            String maGa = String.valueOf(tblGTG.getValueAt(selectedIndex, 0));
            new SuaGTG(maGa).setVisible(true);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnTraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Nhập mã ga cần tra cứu:");
        if(input != null && input.length() > 0){
            gatgs = gtgService.findGaTGByMaGa(input);
            tblModel.setRowCount(0);
            setTableData(gatgs);
        } else{
            tblModel.setRowCount(0);
            setTableData(gtgService.getGTGByMaTT(matt));
        }
    }//GEN-LAST:event_btnTraActionPerformed

