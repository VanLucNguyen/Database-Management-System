    private void setTableData(List<GaTrungGian> gatgs){
        for (GaTrungGian gatg: gatgs){
            tblModel.addRow(new Object[]{gatg.getMaGa(), gatg.getMaTT(), gatg.getThoiGianDung()});
        }
    }
    

