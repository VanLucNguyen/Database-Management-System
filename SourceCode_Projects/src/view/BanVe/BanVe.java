    private void setTableData(List<TuyenTau> tuyenTau){
        for (TuyenTau tuyentau : tuyenTau){
            defaultTableModel.addRow(new Object[]{tuyentau.getMaTT(), tuyentau.getTenTT(), 
               tuyentau.getTGbatdau(), tuyentau.getTGkethuc(), tuyentau.getLoaiTuyen(), tuyentau.getGiaTuyen(), 
               tuyentau.getTGcho()});
        }
    }
