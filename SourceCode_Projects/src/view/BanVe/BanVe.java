        if(row == -1){
            JOptionPane.showMessageDialog(BanVe.this, "Vui lòng chọn tuyến tàu muốn đi", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            String MaTuyen = String.valueOf(ChonTuyenTable.getValueAt(row, 0));
             
             Ve ve = new Ve(MaTuyen);
             ve.setVisible(true);
             this.dispose();
        }       
