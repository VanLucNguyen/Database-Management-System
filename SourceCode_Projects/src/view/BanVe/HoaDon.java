    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
              
        String loaive = txtLoaiVe.getText();
        
        if("Vé Thường".equals(loaive)){
            
            thongtinve.setMaVe(txtMaVe.getText());
            thongtinve.setThoiDiemDungVe(null);
            thongtinve.setTinhTrangVe("Chưa sử dụng");
            thongtinve.setMaKhachHang(txtMaKH.getText());
            
            banveService.ThemVeThuong(thongtinve);
        }
        if("Vé Tháng".equals(loaive)){
            
            thongtinve.setMaVe(txtMaVe.getText());
            thongtinve.setSoLanSD(0);
            thongtinve.setTinhTrangVe("Còn hạn");
            thongtinve.setNgayMua(Date.valueOf(LocalDate.now()));
            thongtinve.setNgayKetThuc(null);
            thongtinve.setMaKhachHang(txtMaKH.getText());
            
            banveService.ThemVeThang(thongtinve);
        }
        JOptionPane.showMessageDialog(rootPane,"Thanh Toán Thành Công!");  
        
        
        
    }//GEN-LAST:event_btnThanhToanActionPerformed

