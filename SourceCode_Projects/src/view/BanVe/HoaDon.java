    private void btnInVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInVeActionPerformed

        try {

            String file_name= "C:\\Users\\nampr\\Desktop\\HoaDon.pdf";
            Document document = new Document() ;
            PdfWriter.getInstance(document, new FileOutputStream(file_name));
            document.open();
            PdfPTable table= new PdfPTable(6);        
           
            PdfPCell c1 = new PdfPCell(new Phrase("TEN KHACH HANG"));
            document.newPage();
            table.addCell(c1);
            c1 = new PdfPCell(new Phrase("SDT"));
            table.addCell(c1);
            c1 = new PdfPCell(new Phrase("MA VE"));
            table.addCell(c1);
            c1 = new PdfPCell(new Phrase("LOAI VE"));
            table.addCell(c1);
            c1 = new PdfPCell(new Phrase("TEN TUYEN"));
            table.addCell(c1);
            c1 = new PdfPCell(new Phrase("NGAY MUA"));
            table.addCell(c1);

            table.addCell(tenKH);
            table.addCell(SDT);
            table.addCell(txtMaVe.getText());
            table.addCell(txtLoaiVe.getText());
            table.addCell(tenTuyen);
            table.addCell(String.valueOf((LocalDate.now())));

            document.add(table);

            document.close();
            
        } catch (Exception e) {
            System.err.println(e);
        }
        JOptionPane.showMessageDialog(rootPane, "In vé thành công!");
        this.dispose();
        ChonGa chonga = new ChonGa();
        chonga.setVisible(true);
    }//GEN-LAST:event_btnInVeActionPerformed

